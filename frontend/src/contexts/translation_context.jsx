export const TranslationProvider = ({ children }) => {
  const [language, setLanguage] = useState(() => {
    try {
      return typeof window !== 'undefined'
        ? normalizeLanguage(localStorage.getItem('language') || 'en')
        : 'en';
    } catch (error) {
      console.error('Failed to read language from storage:', error);
      return 'en';
    }
  });
  const [darkMode, setDarkMode] = useState(() => {
    try {
      return typeof window !== 'undefined' ? localStorage.getItem('darkMode') === 'true' : false;
    } catch (error) {
      console.error('Failed to read dark mode from storage:', error);
      return false;
    }
  });

  useEffect(() => {
    try {
      if (typeof window !== 'undefined') {
        localStorage.setItem('language', language);
      }
    } catch (error) {
      console.error('Failed to save language to storage:', error);
    }

    if (typeof document !== 'undefined') {
      document.documentElement.lang = language;
    }
  }, [language]);

  useEffect(() => {
    try {
      if (typeof window !== 'undefined') {
        localStorage.setItem('darkMode', darkMode);
      }
    } catch (error) {
      console.error('Failed to save dark mode to storage:', error);
    }

    if (typeof document !== 'undefined') {
      if (darkMode) {
        document.body.classList.add('dark-mode');
      } else {
        document.body.classList.remove('dark-mode');
      }
    }
  }, [darkMode]);

  const t = useMemo(() => {
    const currentTranslations = keyTranslations[language] || keyTranslations.en;

    return (key) => {
      if (typeof key !== 'string' || !key) return key;
      return currentTranslations[key] || keyTranslations.en[key] || key;
    };
  }, [language]);

  const ta = useMemo(() => {
    const phraseMap = adminPhraseTranslations[language] || {};
    const entries = Object.entries(phraseMap).sort((left, right) => right[0].length - left[0].length);

    return (text) => {
      if (typeof text !== 'string' || !text) return text;
      if (entries.length === 0) return text;

      let next = text;
      for (const [en, translated] of entries) {
        if (next.includes(en)) {
          next = next.split(en).join(translated);
        }
      }
      return next;
    };
  }, [language]);

  const changeLanguage = (lang) => {
    setLanguage(normalizeLanguage(lang));
  };

  return (
    <TranslationContext.Provider
      value={{
        language,
        isKhmer: language === 'km',
        t,
        ta,
        changeLanguage,
        translations: keyTranslations[language] || keyTranslations.en
      }}
    >
      {children}
    </TranslationContext.Provider>
  );
};

export const useTranslation = () => {
  const context = useContext(TranslationContext);
  if (!context) {
    throw new Error('useTranslation must be used within a TranslationProvider');
  }
  return context;
};
